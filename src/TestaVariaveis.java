
public class TestaVariaveis {

	public static void main(String[] args) {
		
		int idade;
				
		idade = 50;
		
		System.out.println("essa é a sua idade " + idade);
		

		idade = 3 * (10 + 15);
		
		System.out.println(idade);

		double pi;
		
		pi = 3.14159;
				
		System.out.println("o valor de pi é " + pi);
		
		int maximoInteiro = 2147483647;

		System.out.println(maximoInteiro);

		long meuLong = 3000000000l;
		
		System.out.println(meuLong);
		
		float temperatura = 50.5f;

		System.out.println("a temperatura é " + temperatura);
		
		double calculo = 5.0 / 2;
		
		System.out.println(calculo);
		
	}
	
}
